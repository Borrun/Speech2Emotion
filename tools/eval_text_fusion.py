"""
Evaluate stream_online outputs against ground-truth labels_new.jsonl.
Compares multiple "tagged" runs (e.g. baseline vs tf) side-by-side.

Usage
-----
  python tools/eval_text_fusion.py --tags baseline tf

Metrics (per tag, averaged over all labeled files)
---------------------------------------------------
  type_acc   : fraction of frames whose stable emotion type matches GT
  level_mae  : mean |predicted_level_id - GT_level_id|
  constrained: fraction of frames where text constraint fired (tf runs only)

Also prints a per-file breakdown for easy diagnosis.
"""

import argparse
import json
import os
import sys
from collections import defaultdict
from typing import Dict, List, Optional, Tuple

sys.path.append(os.path.abspath(os.path.join(os.path.dirname(__file__), "..")))

LABELS_PATH = "./annotater/labels_new.jsonl"
OUT_DIR     = "./outputs/stream_online"

TYPE_MAP = ["happy", "sad", "angry", "fear", "calm", "confused"]
TYPE_IDX = {t: i for i, t in enumerate(TYPE_MAP)}


# ── ground-truth helpers ─────────────────────────────────────────────────────

def value_to_level(v: float) -> int:
    v = float(v)
    if v >= 111: return 5
    if v >= 86:  return 4
    if v >= 51:  return 3
    if v >= 26:  return 2
    if v >= 11:  return 1
    return 0


def curve_to_frames(curve: list, fps: int, n_frames: int) -> List[Tuple[int, int]]:
    """Step function: between two keyframes, hold the earlier one's (type, level)."""
    pts = sorted(curve, key=lambda p: float(p["t"]))
    result = []
    for f in range(n_frames):
        t = f / float(fps)
        cur_type = pts[0]["type"]
        cur_val  = float(pts[0]["value"])
        for pt in pts:
            if float(pt["t"]) <= t:
                cur_type = pt["type"]
                cur_val  = float(pt["value"])
            else:
                break
        type_id  = TYPE_IDX.get(cur_type, 4)   # default calm
        level_id = value_to_level(cur_val)
        result.append((type_id, level_id))
    return result


def load_labels(path: str) -> Dict[str, dict]:
    labels = {}
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            obj  = json.loads(line)
            wav  = str(obj["wav"])
            labels[wav] = obj
    return labels


# ── prediction helpers ───────────────────────────────────────────────────────

def load_stream_result(path: str) -> Optional[dict]:
    if not os.path.isfile(path):
        return None
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def frames_from_result(res: dict) -> List[Tuple[int, int]]:
    """Extract stable (type_id, level_id) per frame from stream result."""
    out = []
    for fr in res.get("frames", []):
        st = fr.get("stable", {})
        out.append((int(st.get("type_id", 4)), int(st.get("level_id", 0))))
    return out


# ── per-file metrics ─────────────────────────────────────────────────────────

def compute_metrics(gt: List[Tuple[int,int]], pred: List[Tuple[int,int]],
                    constrained_flags: List[bool]) -> dict:
    n = min(len(gt), len(pred))
    if n == 0:
        return {"type_acc": 0.0, "level_mae": 0.0, "constrained": 0.0, "n": 0}

    type_correct = sum(1 for i in range(n) if gt[i][0] == pred[i][0])
    level_ae     = sum(abs(gt[i][1] - pred[i][1]) for i in range(n))
    constrained  = sum(1 for i in range(min(n, len(constrained_flags)))
                       if constrained_flags[i])

    return {
        "type_acc":   type_correct / n,
        "level_mae":  level_ae / n,
        "constrained": constrained / n,
        "n":          n,
    }


def confusion_counts(gt: List[Tuple[int,int]], pred: List[Tuple[int,int]],
                     n_types: int = 6) -> List[List[int]]:
    mat = [[0]*n_types for _ in range(n_types)]
    for i in range(min(len(gt), len(pred))):
        mat[gt[i][0]][pred[i][0]] += 1
    return mat


# ── main ─────────────────────────────────────────────────────────────────────

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--tags", nargs="+", default=["baseline", "tf"],
                    help="run tags to compare (must have been generated by scan_stream_all.py)")
    ap.add_argument("--labels", default=LABELS_PATH)
    ap.add_argument("--out_dir", default=OUT_DIR)
    args = ap.parse_args()

    labels = load_labels(args.labels)
    if not labels:
        print(f"[error] No labels found at {args.labels}")
        return

    # For each tag, map wav_stem → result file path
    def result_path(wav: str, tag: str) -> str:
        stem = os.path.splitext(wav)[0]
        return os.path.join(args.out_dir, f"{stem}.stream_online.{tag}.json")

    # ── per-file table ───────────────────────────────────────────────────────
    tag_totals: Dict[str, dict] = {
        t: {"type_acc": 0.0, "level_mae": 0.0, "constrained": 0.0, "count": 0}
        for t in args.tags
    }

    col_w = 12
    header_parts = [f"{'wav':<22}"]
    for tag in args.tags:
        header_parts += [f"{'type_acc['+tag+']':>{col_w}}",
                         f"{'lvl_mae['+tag+']':>{col_w}}"]
        if tag != args.tags[0]:
            header_parts += [f"{'Δtype_acc':>{col_w}}", f"{'Δlvl_mae':>{col_w}}"]
    if len(args.tags) >= 2:
        header_parts += [f"{'constrained%':>{col_w+2}}"]
    print("\n" + "  ".join(header_parts))
    print("-" * (24 + len(args.tags) * (col_w + 2) * 2 + 10))

    per_file_metrics: Dict[str, Dict[str, dict]] = defaultdict(dict)

    for wav, label in sorted(labels.items()):
        fps      = int(label.get("fps", 30))
        n_frames_gt = int(round(float(label["duration"]) * fps))
        gt_frames   = curve_to_frames(label["curve"], fps, n_frames_gt)

        row_parts = [f"{wav:<22}"]
        first_tag_metrics = None

        for tag in args.tags:
            rpath = result_path(wav, tag)
            res   = load_stream_result(rpath)
            if res is None:
                row_parts += [f"{'(missing)':>{col_w}}", f"{'':>{col_w}}"]
                continue

            pred_frames = frames_from_result(res)
            cf_flags    = [fr.get("text_constrained", False) for fr in res.get("frames", [])]
            m = compute_metrics(gt_frames, pred_frames, cf_flags)
            per_file_metrics[wav][tag] = m

            tag_totals[tag]["type_acc"]   += m["type_acc"]
            tag_totals[tag]["level_mae"]  += m["level_mae"]
            tag_totals[tag]["constrained"]+= m["constrained"]
            tag_totals[tag]["count"]      += 1

            row_parts += [f"{m['type_acc']:>{col_w}.3f}",
                          f"{m['level_mae']:>{col_w}.3f}"]

            if first_tag_metrics is None:
                first_tag_metrics = m
            elif tag != args.tags[0]:
                d_acc = m["type_acc"] - first_tag_metrics["type_acc"]
                d_mae = m["level_mae"] - first_tag_metrics["level_mae"]
                row_parts += [
                    f"{('+' if d_acc>=0 else '')+f'{d_acc:.3f}':>{col_w}}",
                    f"{('+' if d_mae>=0 else '')+f'{d_mae:.3f}':>{col_w}}",
                ]

        if len(args.tags) >= 2:
            last_tag = args.tags[-1]
            if last_tag in per_file_metrics.get(wav, {}):
                c = per_file_metrics[wav][last_tag]["constrained"]
                row_parts += [f"{c*100:>{col_w+2}.1f}%"]

        print("  ".join(row_parts))

    # ── aggregate summary ────────────────────────────────────────────────────
    print("\n" + "=" * 70)
    print(f"{'SUMMARY (train set, '+str(len(labels))+' files)':^70}")
    print("=" * 70)
    print(f"{'tag':<16} {'type_acc':>10} {'level_mae':>10} {'constrained%':>14}")
    print("-" * 54)

    baseline_acc = baseline_mae = None
    for tag in args.tags:
        t = tag_totals[tag]
        n = max(1, t["count"])
        acc  = t["type_acc"]  / n
        mae  = t["level_mae"] / n
        cons = t["constrained"] / n * 100

        if baseline_acc is None:
            baseline_acc = acc
            baseline_mae = mae
            delta_str = ""
        else:
            d_acc = acc - baseline_acc
            d_mae = mae - baseline_mae
            delta_str = (f"  Δacc={'+' if d_acc>=0 else ''}{d_acc:.3f}"
                         f"  Δmae={'+' if d_mae>=0 else ''}{d_mae:.3f}")

        print(f"  {tag:<14} {acc:>10.3f} {mae:>10.3f} {cons:>13.1f}%{delta_str}")

    # ── per-type accuracy breakdown ──────────────────────────────────────────
    print("\n── per-emotion type accuracy (last tag vs baseline) ──")
    print(f"{'type':<12}", end="")
    for tag in args.tags:
        print(f"  {tag:>10}", end="")
    print()

    type_correct: Dict[str, Dict[int, int]] = {t: defaultdict(int) for t in args.tags}
    type_total:   Dict[int, int] = defaultdict(int)

    for wav, label in labels.items():
        fps         = int(label.get("fps", 30))
        n_frames_gt = int(round(float(label["duration"]) * fps))
        gt_frames   = curve_to_frames(label["curve"], fps, n_frames_gt)

        for i, (gt_t, gt_l) in enumerate(gt_frames):
            type_total[gt_t] += 1
            for tag in args.tags:
                rpath = result_path(wav, tag)
                res   = load_stream_result(rpath)
                if res is None or i >= len(res.get("frames", [])):
                    continue
                pred_t = int(res["frames"][i].get("stable", {}).get("type_id", 4))
                if pred_t == gt_t:
                    type_correct[tag][gt_t] += 1

    for tid, tname in enumerate(TYPE_MAP):
        n = type_total[tid]
        if n == 0:
            continue
        print(f"  {tname:<10}", end="")
        for tag in args.tags:
            acc = type_correct[tag][tid] / n
            print(f"  {acc:>10.3f}", end="")
        print(f"  (n={n})")

    print()


if __name__ == "__main__":
    main()
